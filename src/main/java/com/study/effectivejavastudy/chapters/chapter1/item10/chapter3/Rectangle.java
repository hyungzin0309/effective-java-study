package com.study.effectivejavastudy.chapters.chapter1.item10.chapter3;

public class Rectangle {

    int height;
    int width;

    public void setHeight(int height) {
        this.height = height;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getArea() {
        return height * width;
    }

}
